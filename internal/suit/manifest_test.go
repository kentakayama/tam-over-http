func TestSUITManifest_UnmarshalUntaggedEnvelope(t *testing.T) {
	var e Envelope
	_, untaggedManifest := e.SkipTag(taggedManifest)
	err := cbor.Unmarshal(untaggedManifest, &e)
	require.Nil(t, err)

	assert.Equal(t, false, e.Tagged)
}

func TestSUITManifest_Verify(t *testing.T) {
	var e Envelope
	_, untaggedManifest := e.SkipTag(taggedManifest)
	err := cbor.Unmarshal(untaggedManifest, &e)
	require.Nil(t, err)

	err = e.Verify(&key)
	assert.Nil(t, err)
}

func TestSUITManifest_UnmarshalManifest(t *testing.T) {
	var e Envelope
	err := cbor.Unmarshal(taggedManifest, &e)
	require.Nil(t, err)

	var m Nested[Manifest]
	err = cbor.Unmarshal(e.ManifestBstr, &m)
	require.Nil(t, err)

	assert.Equal(t, uint64(0), m.Value.ManifestSequenceNumber)
	componentID := ComponentID{{
		0x68, 0x65, 0x6c, 0x6c, 0x6f, 0x2e, 0x74, 0x78, 0x74, // 'hello.txt'
	}}
	assert.Equal(t, componentID, m.Value.Common.Value.Components[0])

	encodedComponentID, err := cbor.Marshal(componentID)
	require.Nil(t, err)
	assert.Equal(t, []byte{
		0x81,                                                       // [
		0x49, 0x68, 0x65, 0x6c, 0x6c, 0x6f, 0x2e, 0x74, 0x78, 0x74, // 'hello.txt'
	}, encodedComponentID)
}
