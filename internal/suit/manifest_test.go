func TestSUITManifest_Thumbprint(t *testing.T) {
	k, err := key.Thumbprint(crypto.SHA256)
	require.Nil(t, err)
	assert.Equal(t, kid, k)
	fmt.Printf("KID in hex: %s\n", hex.EncodeToString(kid))
	fmt.Printf("KID in base64: %s\n", base64.RawStdEncoding.EncodeToString(kid))
}

